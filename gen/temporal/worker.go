package temporal

import (
	"bytes"
	"fmt"
	"sort"

	"github.com/dave/jennifer/jen"

	"github.com/gaarutyunov/sysgo/engine"
)

const (
	sdkClient = "go.temporal.io/sdk/client"
	sdkWorker = "go.temporal.io/sdk/worker"
)

// GenerateWorker emits RunWorker: it creates one task-queue-scoped Temporal
// worker per task queue used by the model's @Workflow actions, registers those
// workflows and the Activities port on each, starts them, and blocks until
// interrupted (specs/TEMPORAL.md §4, T-worker; `perform` part → action →
// task-queue registration).
//
// The activity implementations behind the Activities port are hand-written; the
// caller passes one to RunWorker. The output belongs to the same package as the
// generated workflows and activities.
func GenerateWorker(m *engine.Model, packageName string) (string, error) {
	wfs := Workflows(m)

	// Group workflows by task queue, deterministically.
	byQueue := map[string][]Workflow{}
	for _, wf := range wfs {
		byQueue[wf.TaskQueue] = append(byQueue[wf.TaskQueue], wf)
	}
	queues := make([]string, 0, len(byQueue))
	for q := range byQueue {
		queues = append(queues, q)
	}
	sort.Strings(queues)
	for _, q := range queues {
		sort.Slice(byQueue[q], func(i, j int) bool { return byQueue[q][i].Name() < byQueue[q][j].Name() })
	}

	f := jen.NewFile(packageName)
	f.HeaderComment("Code generated by sysgo (gen/temporal). DO NOT EDIT.")

	f.Comment("RunWorker registers the workflows and activities on their task queues and")
	f.Comment("blocks until interrupted. The activities implementation is the hand-written")
	f.Comment("Activities port.")
	f.Func().Id("RunWorker").Params(
		jen.Id("c").Qual(sdkClient, "Client"),
		jen.Id("acts").Id("Activities"),
	).Error().BlockFunc(func(g *jen.Group) {
		g.Var().Id("workers").Index().Qual(sdkWorker, "Worker")
		for i, q := range queues {
			wv := fmt.Sprintf("w%d", i)
			g.Id(wv).Op(":=").Qual(sdkWorker, "New").Call(
				jen.Id("c"), jen.Lit(q), jen.Qual(sdkWorker, "Options").Values(),
			)
			for _, wf := range byQueue[q] {
				g.Id(wv).Dot("RegisterWorkflow").Call(jen.Id(exported(wf.Name()) + "Workflow"))
			}
			// Register the activities port; Temporal registers each method by
			// name, matching the workflow's ExecuteActivity calls.
			g.Id(wv).Dot("RegisterActivity").Call(jen.Id("acts"))
			g.Id("workers").Op("=").Append(jen.Id("workers"), jen.Id(wv))
		}
		g.For(jen.List(jen.Id("_"), jen.Id("w")).Op(":=").Range().Id("workers")).Block(
			jen.If(
				jen.Err().Op(":=").Id("w").Dot("Start").Call(),
				jen.Err().Op("!=").Nil(),
			).Block(jen.Return(jen.Err())),
		)
		g.Op("<-").Qual(sdkWorker, "InterruptCh").Call()
		g.For(jen.List(jen.Id("_"), jen.Id("w")).Op(":=").Range().Id("workers")).Block(
			jen.Id("w").Dot("Stop").Call(),
		)
		g.Return(jen.Nil())
	})

	var buf bytes.Buffer
	if err := f.Render(&buf); err != nil {
		return "", err
	}
	return buf.String(), nil
}
