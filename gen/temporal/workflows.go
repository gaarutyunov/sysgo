// GenerateWorkflows emits Go source for the model's @Workflow actions. Each
// workflow becomes a function that orchestrates the Activities outbound port
// (from GenerateActivities), calling each activity step in declaration order —
// the succession → sequential mapping (specs/TEMPORAL.md §3, §3.1).
//
// Each workflow is a real Temporal workflow (workflow.Context) whose body calls
// each activity via workflow.ExecuteActivity by registered name, in declaration
// order — the succession → sequential mapping (§3, §3.1). Activity invocation
// through the SDK (no direct side effects) is the determinism baseline (§6-A);
// the activity implementations behind those names are the hand-written ports.
//
// The output belongs to the same package as GenerateActivities; the worker
// (GenerateWorker) registers these workflow functions and the Activities port.
// sdkWorkflow is the Temporal workflow package the generated workflow bodies use.
const sdkWorkflow = "go.temporal.io/sdk/workflow"

func GenerateWorkflows(m *engine.Model, packageName string) (string, error) {
	wfs := Workflows(m)
	sort.Slice(wfs, func(i, j int) bool { return wfs[i].Name() < wfs[j].Name() })

	f := jen.NewFile(packageName)
	f.HeaderComment("Code generated by sysgo (gen/temporal). DO NOT EDIT.")

	for _, wf := range wfs {
		params, steps := splitParamsAndSteps(wf.Element)
		timers := timerAccepts(wf.Element)
		guards := guardByStep(wf.Element)
		f.Func().Id(exported(wf.Name()) + "Workflow").ParamsFunc(func(p *jen.Group) {
			p.Id("ctx").Qual(sdkWorkflow, "Context")
			for _, param := range params {
				typeName, _, _ := goType(param)
				p.Id(param.Name()).Id(typeName)
			}
		}).Error().BlockFunc(func(g *jen.Group) {
			if len(steps) > 0 {
				// Apply default activity options; per-activity @Timeout/@RetryPolicy
				// wiring is carried by the Options map (GenerateActivities).
				g.Id("ctx").Op("=").Qual(sdkWorkflow, "WithActivityOptions").Call(
					jen.Id("ctx"),
					jen.Qual(sdkWorkflow, "ActivityOptions").Values(jen.Dict{
						jen.Id("StartToCloseTimeout"): jen.Qual("time", "Minute"),
					}),
				)
			}
			for _, it := range orderedBody(steps, timers) {
				if it.timer != nil {
					dur, ok := timerDuration(*it.timer)
					if !ok {
						g.Commentf("accept %s %s: unsupported timer expression", it.timer.Mode, it.timer.Ref)
						continue
					}
					g.If(
						jen.Err().Op(":=").Qual(sdkWorkflow, "Sleep").Call(jen.Id("ctx"), dur),
						jen.Err().Op("!=").Nil(),
					).Block(jen.Return(jen.Err()))
					continue
				}
				step := it.step
				exec := jen.If(
					jen.Err().Op(":=").Qual(sdkWorkflow, "ExecuteActivity").CallFunc(func(c *jen.Group) {
						c.Id("ctx")
						c.Lit(exported(step.activity.Name()))
						for _, arg := range stepArgs(params, attributes(step.activity)) {
							c.Add(arg)
						}
					}).Dot("Get").Call(jen.Id("ctx"), jen.Nil()),
					jen.Err().Op("!=").Nil(),
				).Block(
					jen.Return(jen.Err()),
				)
				// A guarded succession targeting this step runs it conditionally.
				if guard := guards[stepName(step)]; guard != "" {
					g.If(jen.Id(guard)).Block(exec)
				} else {
					g.Add(exec)
				}
			}
			g.Return(jen.Nil())
		})
	}

	var buf bytes.Buffer
	if err := f.Render(&buf); err != nil {
		return "", err
	}
	return buf.String(), nil
}

// guardByStep maps a workflow's guarded-succession target name to its guard
// condition, so the target activity can be emitted conditionally.
func guardByStep(wf engine.Element) map[string]string {
	out := map[string]string{}
	for _, su := range wf.Successions() {
		if su.Guard != "" && su.TargetName != "" {
			out[su.TargetName] = su.Guard
		}
	}
	return out
}

// stepName returns an activity step's usage name (e.g. "b" for `action b : B`),
// or "" for a perform-based step.
func stepName(st *step) string {
	if st.usage.IsValid() {
		return st.usage.Name()
	}
	return ""
}

// bodyItem is one ordered element of a workflow body: an activity step or a
// durable timer.
type bodyItem struct {
	start text.TextSize
	step  *step
	timer *engine.Accept
}

// orderedBody merges usage-based activity steps and timer accepts in source
// order; perform-based steps (which have no source usage) are appended after.
func orderedBody(steps []step, timers []engine.Accept) []bodyItem {
	var ordered, performs []bodyItem
	for i := range steps {
		st := &steps[i]
		if st.usage.IsValid() {
			ordered = append(ordered, bodyItem{start: st.usage.Range().Start, step: st})
		} else {
			performs = append(performs, bodyItem{step: st})
		}
	}
	for i := range timers {
		ordered = append(ordered, bodyItem{start: timers[i].Range.Start, timer: &timers[i]})
	}
	sort.SliceStable(ordered, func(i, j int) bool { return ordered[i].start < ordered[j].start })
	return append(ordered, performs...)
}

// timerAccepts returns a workflow body's durable-timer accept statements
// (`accept after` / `accept at`).
func timerAccepts(wf engine.Element) []engine.Accept {
	var out []engine.Accept
	for _, a := range wf.Accepts() {
		if a.IsTimer() {
			out = append(out, a)
		}
	}
	return out
}

// timerDuration renders the Go duration for a timer accept. A bare integer is
// interpreted as seconds; non-literal durations are not yet supported.
func timerDuration(a engine.Accept) (jen.Code, bool) {
	if n, err := strconv.Atoi(strings.TrimSpace(a.Ref)); err == nil {
		return jen.Lit(n).Op("*").Qual("time", "Second"), true
	}
	return nil, false
}

// step is one activity call within a workflow body.
type step struct {
	usage    engine.Element // the child usage (e.g. `action charge : ChargeCard`)
	activity engine.Element // the @Activity definition it is typed by
}

// splitParamsAndSteps partitions a workflow action's child usages into
// parameters (typed by data/other definitions) and activity steps (typed by an
// @Activity definition), preserving declaration order.
func splitParamsAndSteps(wf engine.Element) (params []engine.Element, steps []step) {
	for _, child := range wf.Children() {
		if child.Kind() != engine.ElementUsage {
			continue
		}
		if act, ok := stepActivity(child); ok {
			steps = append(steps, step{usage: child, activity: act})
			continue
		}
		params = append(params, child)
	}
	// `perform` statements targeting an @Activity are activity steps too.
	for _, pf := range wf.Performs() {
		if tgt, ok := pf.Target(); ok {
			if _, isActivity := tgt.Metadata("Activity"); isActivity {
				steps = append(steps, step{activity: tgt})
			}
		}
	}
	return params, steps
}

// stepActivity returns the @Activity definition a child usage is typed by, if
// any.
func stepActivity(child engine.Element) (engine.Element, bool) {
	t, ok := typedTarget(child)
	if !ok {
		return engine.Element{}, false
	}
	if _, isActivity := t.Metadata("Activity"); isActivity {
		return t, true
	}
	return engine.Element{}, false
}

// stepArgs matches each of an activity method's parameters to a workflow
// parameter of the same Go type, falling back to a zero value so the call
// always compiles.
func stepArgs(wfParams, activityParams []engine.Element) []jen.Code {
	var args []jen.Code
	for _, ap := range activityParams {
		apType, _, _ := goType(ap)
		matched := ""
		for _, wp := range wfParams {
			if wpType, _, _ := goType(wp); wpType == apType {
				matched = wp.Name()
				break
			}
		}
		if matched != "" {
			args = append(args, jen.Id(matched))
		} else {
			args = append(args, zeroValue(apType))
		}
	}
	return args
}

// zeroValue returns the Go zero value for a mapped type.
func zeroValue(goType string) jen.Code {
	switch goType {
	case "string":
		return jen.Lit("")
	case "bool":
		return jen.False()
	case "float64":
		return jen.Lit(0.0)
	case "int64":
		return jen.Lit(0)
	default:
		return jen.Id(goType).Values() // struct zero value, e.g. Order{}
	}
}
