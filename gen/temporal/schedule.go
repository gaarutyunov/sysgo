package temporal

import (
	"bytes"
	"sort"

	"github.com/dave/jennifer/jen"

	"github.com/gaarutyunov/sysgo/engine"
)

// Schedule is a @Workflow that also carries @Schedule: the workflow plus its
// cron spec and optional jitter.
type Schedule struct {
	Workflow Workflow
	Spec     string // cron expression
	Jitter   string // Go duration string, or ""
}

// Schedules returns every @Workflow annotated with @Schedule.
func Schedules(m *engine.Model) []Schedule {
	var out []Schedule
	for _, wf := range Workflows(m) {
		if meta, ok := wf.Element.Metadata("Schedule"); ok {
			out = append(out, Schedule{
				Workflow: wf,
				Spec:     val(meta, "spec"),
				Jitter:   val(meta, "jitter"),
			})
		}
	}
	return out
}

// GenerateSchedules emits, for each @Schedule workflow, a Create<WF>Schedule
// function that registers a Temporal Schedule running the workflow on its task
// queue per the cron spec (with optional jitter) — specs/TEMPORAL.md §4.
func GenerateSchedules(m *engine.Model, packageName string) (string, error) {
	scheds := Schedules(m)
	sort.Slice(scheds, func(i, j int) bool { return scheds[i].Workflow.Name() < scheds[j].Workflow.Name() })

	f := jen.NewFile(packageName)
	f.HeaderComment("Code generated by sysgo (gen/temporal). DO NOT EDIT.")

	needDuration := false
	for _, s := range scheds {
		if s.Jitter != "" {
			needDuration = true
		}
	}

	for _, s := range scheds {
		wf := exported(s.Workflow.Name())
		id := s.Workflow.ID
		if id == "" {
			id = wf
		}
		s := s // capture
		f.Commentf("Create%sSchedule registers a Temporal Schedule that runs %sWorkflow.", wf, wf)
		f.Func().Id("Create"+wf+"Schedule").Params(
			jen.Id("ctx").Qual("context", "Context"),
			jen.Id("c").Qual(sdkClient, "Client"),
		).Error().BlockFunc(func(g *jen.Group) {
			g.List(jen.Id("_"), jen.Err()).Op(":=").Id("c").Dot("ScheduleClient").Call().Dot("Create").Call(
				jen.Id("ctx"),
				jen.Qual(sdkClient, "ScheduleOptions").Values(jen.Dict{
					jen.Id("ID"):     jen.Lit(id + "-schedule"),
					jen.Id("Spec"):   scheduleSpec(s),
					jen.Id("Action"): scheduleAction(s, wf, id),
				}),
			)
			g.Return(jen.Err())
		})
	}

	if needDuration {
		f.Comment("mustDuration parses a Go duration string, returning 0 on a parse error.")
		f.Func().Id("mustDuration").Params(jen.Id("s").String()).Qual("time", "Duration").Block(
			jen.List(jen.Id("d"), jen.Id("_")).Op(":=").Qual("time", "ParseDuration").Call(jen.Id("s")),
			jen.Return(jen.Id("d")),
		)
	}

	var buf bytes.Buffer
	if err := f.Render(&buf); err != nil {
		return "", err
	}
	return buf.String(), nil
}

// scheduleSpec renders the client.ScheduleSpec for a schedule.
func scheduleSpec(s Schedule) jen.Code {
	return jen.Qual(sdkClient, "ScheduleSpec").Values(jen.DictFunc(func(d jen.Dict) {
		if s.Spec != "" {
			d[jen.Id("CronExpressions")] = jen.Index().String().Values(jen.Lit(s.Spec))
		}
		if s.Jitter != "" {
			d[jen.Id("Jitter")] = jen.Id("mustDuration").Call(jen.Lit(s.Jitter))
		}
	}))
}

// scheduleAction renders the &client.ScheduleWorkflowAction binding the workflow
// to the schedule on its task queue.
func scheduleAction(s Schedule, wf, id string) jen.Code {
	return jen.Op("&").Qual(sdkClient, "ScheduleWorkflowAction").Values(jen.Dict{
		jen.Id("ID"):        jen.Lit(id),
		jen.Id("Workflow"):  jen.Id(wf + "Workflow"),
		jen.Id("TaskQueue"): jen.Lit(s.Workflow.TaskQueue),
	})
}
