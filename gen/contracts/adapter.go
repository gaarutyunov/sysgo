package contracts

import (
	"bytes"
	"sort"
	"strings"

	"github.com/dave/jennifer/jen"

	"github.com/gaarutyunov/sysgo/engine"
)

// restOp is a resolved @REST transport operation: its name, success status, the
// Go model types of its single directed request/response bodies (empty when
// absent), and the domain action it performs (empty when none).
type restOp struct {
	opName  string // the operation action name, e.g. "placeOrder"
	status  string // success HTTP status, e.g. "201"
	inType  string // Go model type of the in-param body, or ""
	outType string // Go model type of the out-param body, or ""
	domain  string // performed domain action name, or ""
	wired   bool   // has a domain action and ≤1 definition-typed in/out param
}

// GenerateAdapter emits the transport adapter: a Domain inbound-port interface
// (one method per performed domain action) and an Adapter struct implementing
// oapi-codegen's StrictServerInterface, where each operation method binds the
// request body to the domain action and maps its result to the typed response.
//
// The adapter is generated into the same package as the oapi-codegen output so
// it can reference the generated ServerInterface, request/response objects, and
// model types directly. The domain use-case bodies stay hand-written behind the
// Domain port.
func GenerateAdapter(m *engine.Model, packageName string) (string, error) {
	ops := collectRestOps(m)

	f := jen.NewFile(packageName)
	f.HeaderComment("Code generated by sysgo (gen/contracts). DO NOT EDIT.")

	emitDomainInterface(f, ops)

	// Adapter struct, constructor, and a compile-time interface assertion.
	f.Comment("Adapter binds transport operations to the domain port. It implements")
	f.Comment("the generated StrictServerInterface.")
	f.Type().Id("Adapter").Struct(jen.Id("domain").Id("Domain"))
	f.Func().Id("NewAdapter").Params(jen.Id("domain").Id("Domain")).Id("Adapter").Block(
		jen.Return(jen.Id("Adapter").Values(jen.Dict{jen.Id("domain"): jen.Id("domain")})),
	)
	f.Var().Op("_").Id("StrictServerInterface").Op("=").Id("Adapter").Values()

	for _, op := range ops {
		emitAdapterMethod(f, op)
	}

	var buf bytes.Buffer
	if err := f.Render(&buf); err != nil {
		return "", err
	}
	return buf.String(), nil
}

// emitDomainInterface renders the Domain port with one method per distinct
// performed domain action, its signature derived from the operation's directed
// bodies.
func emitDomainInterface(f *jen.File, ops []restOp) {
	seen := map[string]bool{}
	var methods []restOp
	for _, op := range ops {
		if !op.wired || seen[op.domain] {
			continue
		}
		seen[op.domain] = true
		methods = append(methods, op)
	}
	sort.Slice(methods, func(i, j int) bool { return methods[i].domain < methods[j].domain })

	f.Comment("Domain is the inbound port the transport adapter drives. Each method is")
	f.Comment("a domain action performed by one or more @REST operations; the")
	f.Comment("implementations (use-case bodies) are hand-written.")
	f.Type().Id("Domain").InterfaceFunc(func(g *jen.Group) {
		for _, mth := range methods {
			g.Id(exported(mth.domain)).
				ParamsFunc(func(p *jen.Group) {
					p.Id("ctx").Qual("context", "Context")
					if mth.inType != "" {
						p.Id("in").Id(mth.inType)
					}
				}).
				ParamsFunc(func(r *jen.Group) {
					if mth.outType != "" {
						r.Id(mth.outType)
					}
					r.Error()
				})
		}
	})
}

// emitAdapterMethod renders one StrictServerInterface method for op.
func emitAdapterMethod(f *jen.File, op restOp) {
	exp := exported(op.opName)
	f.Func().Params(jen.Id("a").Id("Adapter")).Id(exp).
		Params(
			jen.Id("ctx").Qual("context", "Context"),
			jen.Id("request").Id(exp+"RequestObject"),
		).
		Params(jen.Id(exp+"ResponseObject"), jen.Error()).
		BlockFunc(func(g *jen.Group) {
			if !op.wired {
				g.Return(
					jen.Nil(),
					jen.Qual("fmt", "Errorf").Call(jen.Lit("operation "+op.opName+" has no domain action to perform")),
				)
				return
			}
			call := jen.Id("a").Dot("domain").Dot(exported(op.domain)).CallFunc(func(c *jen.Group) {
				c.Id("ctx")
				if op.inType != "" {
					c.Op("*").Id("request").Dot("Body")
				}
			})
			if op.outType != "" {
				respType := exp + op.status + "JSONResponse"
				g.List(jen.Id("out"), jen.Err()).Op(":=").Add(call)
				g.If(jen.Err().Op("!=").Nil()).Block(jen.Return(jen.Nil(), jen.Err()))
				g.Return(jen.Id(respType).Call(jen.Id("out")), jen.Nil())
			} else {
				respType := exp + op.status + "Response"
				g.If(jen.Err().Op(":=").Add(call).Op(";").Err().Op("!=").Nil()).Block(
					jen.Return(jen.Nil(), jen.Err()),
				)
				g.Return(jen.Id(respType).Values(), jen.Nil())
			}
		})
}

// collectRestOps gathers every @REST operation in the user packages, resolving
// its success status, single directed body types, and performed domain action.
func collectRestOps(m *engine.Model) []restOp {
	var ops []restOp
	for _, top := range m.Root().Children() {
		if isBundledPackage(top.Name()) {
			continue
		}
		walk(top, func(e engine.Element) {
			meta, ok := e.Metadata("REST")
			if !ok {
				return
			}
			op := restOp{opName: e.Name(), status: unquote(valueOr(meta, "successStatus", "200"))}

			var ins, outs []engine.Element
			for _, c := range e.Children() {
				if c.Kind() != engine.ElementUsage {
					continue
				}
				switch c.Direction() {
				case "in":
					ins = append(ins, c)
				case "out":
					outs = append(outs, c)
				}
			}
			ok = len(ins) <= 1 && len(outs) <= 1
			if len(ins) == 1 {
				if t, isDef := paramSchemaType(ins[0]); isDef {
					op.inType = goModelType(t)
				} else {
					ok = false
				}
			}
			if len(outs) == 1 {
				if t, isDef := paramSchemaType(outs[0]); isDef {
					op.outType = goModelType(t)
				} else {
					ok = false
				}
			}
			for _, p := range e.Performs() {
				if t, resolved := p.Target(); resolved {
					op.domain = t.Name()
					break
				}
			}
			op.wired = ok && op.domain != ""
			ops = append(ops, op)
		})
	}
	sort.Slice(ops, func(i, j int) bool { return ops[i].opName < ops[j].opName })
	return ops
}

// goModelType returns the Go type name oapi-codegen generates for a component
// schema definition — its qualified name with the "::" separators removed
// ("API::Order" → "APIOrder").
func goModelType(def engine.Element) string {
	return strings.ReplaceAll(componentName(def.QualifiedName()), ".", "")
}

// exported upper-cases the first letter so a SysML name becomes an exported Go
// identifier.
func exported(name string) string {
	if name == "" {
		return name
	}
	return strings.ToUpper(name[:1]) + name[1:]
}
