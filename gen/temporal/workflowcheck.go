package temporal

import "strings"

// workflowcheckModule is the import path of Temporal's official workflowcheck
// analyzer — a go/analysis pass that flags non-deterministic constructs in
// workflow code.
const workflowcheckModule = "go.temporal.io/sdk/contrib/tools/workflowcheck"

// GenerateWorkflowcheck emits a portable, CI-agnostic shell script that runs
// Temporal's workflowcheck analyzer as a vet-style determinism check over the
// generated workflow package — determinism enforcement B (specs/TEMPORAL.md §6,
// T7). It guards hand-written and hand-edited workflow code against
// non-deterministic constructs at build time.
//
// sysgo emits the tooling config only: the script installs and runs
// workflowcheck in the generated project, so sysgo's own module takes on no
// Temporal dependency. The analyzer version defaults to the latest release; pin
// it per project if reproducibility is required (§10).
func GenerateWorkflowcheck() string {
	var b strings.Builder
	b.WriteString("#!/usr/bin/env bash\n")
	b.WriteString("# Code generated by sysgo (gen/temporal). DO NOT EDIT.\n")
	b.WriteString("#\n")
	b.WriteString("# Determinism enforcement (B): run Temporal's workflowcheck analyzer as a\n")
	b.WriteString("# vet-style check over the generated workflows, guarding against\n")
	b.WriteString("# non-deterministic constructs. See specs/TEMPORAL.md §6.\n")
	b.WriteString("#\n")
	b.WriteString("# Wire this into CI as a required step, e.g. `bash workflowcheck.sh`.\n")
	b.WriteString("set -euo pipefail\n")
	b.WriteString("\n")
	b.WriteString("# Install the analyzer into an isolated GOBIN so it does not perturb the\n")
	b.WriteString("# project's own tool set.\n")
	b.WriteString("bin=\"$(mktemp -d)\"\n")
	b.WriteString("trap 'rm -rf \"$bin\"' EXIT\n")
	b.WriteString("GOBIN=\"$bin\" go install " + workflowcheckModule + "@latest\n")
	b.WriteString("\n")
	b.WriteString("# Run it as a vet tool over every package (workflowcheck only reports on\n")
	b.WriteString("# functions registered as Temporal workflows).\n")
	b.WriteString("go vet \"-vettool=$bin/workflowcheck\" ./...\n")
	return b.String()
}
