package temporal

import (
	"bytes"
	"strconv"
	"strings"

	"github.com/dave/jennifer/jen"

	"github.com/gaarutyunov/sysgo/engine"
)

const (
	sdkConverter   = "go.temporal.io/sdk/converter"
	temporalCommon = "go.temporal.io/api/common/v1"
	protoPkg       = "google.golang.org/protobuf/proto"
)

// externalStorageThreshold returns the smallest @ExternalStorage threshold in
// the model and whether any @ExternalStorage annotation is present. A single
// data converter serves every payload, so the tightest threshold wins.
func externalStorageThreshold(m *engine.Model) (int, bool) {
	found := false
	min := 0
	walkUser(m, func(e engine.Element) {
		meta, ok := e.Metadata("ExternalStorage")
		if !ok {
			return
		}
		n, err := strconv.Atoi(strings.TrimSpace(val(meta, "threshold")))
		if err != nil {
			return
		}
		if !found || n < min {
			min = n
		}
		found = true
	})
	return min, found
}

// GenerateExternalStorage emits the claim-check Payload Codec and data-converter
// wiring for @ExternalStorage payloads (specs/TEMPORAL.md §4). Payloads larger
// than the threshold are off-loaded to a hand-written ExternalStore port (the
// S3 + encryption adapter) and replaced with a small reference; the data
// converter wires the codec onto Temporal's default converter.
//
// If the model has no @ExternalStorage annotation, only the file header is
// emitted.
func GenerateExternalStorage(m *engine.Model, packageName string) (string, error) {
	threshold, ok := externalStorageThreshold(m)

	f := jen.NewFile(packageName)
	f.HeaderComment("Code generated by sysgo (gen/temporal). DO NOT EDIT.")

	if !ok {
		var buf bytes.Buffer
		if err := f.Render(&buf); err != nil {
			return "", err
		}
		return buf.String(), nil
	}

	f.Comment("ExternalStore is the hand-written adapter that persists (and encrypts)")
	f.Comment("claim-checked payloads — e.g. an S3-backed, encrypting store.")
	f.Type().Id("ExternalStore").Interface(
		jen.Id("Put").Params(jen.Id("ref").String(), jen.Id("data").Index().Byte()).Error(),
		jen.Id("Get").Params(jen.Id("ref").String()).Params(jen.Index().Byte(), jen.Error()),
	)

	f.Comment("externalStorageRefKey marks a payload whose data was off-loaded to the")
	f.Comment("ExternalStore; its value is the storage reference.")
	f.Const().Id("externalStorageRefKey").Op("=").Lit("sysgo-external-ref")

	f.Comment("ExternalStorageCodec off-loads payloads larger than Threshold bytes to")
	f.Comment("Store, replacing them with a reference (the claim check).")
	f.Type().Id("ExternalStorageCodec").Struct(
		jen.Id("Threshold").Int(),
		jen.Id("Store").Id("ExternalStore"),
	)

	f.Var().Op("_").Qual(sdkConverter, "PayloadCodec").Op("=").Parens(jen.Op("*").Id("ExternalStorageCodec")).Parens(jen.Nil())

	payloadPtr := jen.Op("*").Qual(temporalCommon, "Payload")

	// Encode
	f.Func().Params(jen.Id("c").Op("*").Id("ExternalStorageCodec")).Id("Encode").
		Params(jen.Id("payloads").Index().Add(payloadPtr)).
		Params(jen.Index().Op("*").Qual(temporalCommon, "Payload"), jen.Error()).
		BlockFunc(func(g *jen.Group) {
			g.Id("out").Op(":=").Make(jen.Index().Op("*").Qual(temporalCommon, "Payload"), jen.Len(jen.Id("payloads")))
			g.For(jen.List(jen.Id("i"), jen.Id("p")).Op(":=").Range().Id("payloads")).BlockFunc(func(b *jen.Group) {
				b.If(jen.Len(jen.Id("p").Dot("Data")).Op("<=").Id("c").Dot("Threshold")).Block(
					jen.Id("out").Index(jen.Id("i")).Op("=").Id("p"),
					jen.Continue(),
				)
				b.List(jen.Id("raw"), jen.Err()).Op(":=").Qual(protoPkg, "Marshal").Call(jen.Id("p"))
				b.If(jen.Err().Op("!=").Nil()).Block(jen.Return(jen.Nil(), jen.Err()))
				b.Id("ref").Op(":=").Qual("fmt", "Sprintf").Call(jen.Lit("%x"), jen.Qual("crypto/sha256", "Sum256").Call(jen.Id("raw")))
				b.If(
					jen.Err().Op(":=").Id("c").Dot("Store").Dot("Put").Call(jen.Id("ref"), jen.Id("raw")),
					jen.Err().Op("!=").Nil(),
				).Block(jen.Return(jen.Nil(), jen.Err()))
				b.Id("out").Index(jen.Id("i")).Op("=").Op("&").Qual(temporalCommon, "Payload").Values(jen.Dict{
					jen.Id("Metadata"): jen.Map(jen.String()).Index().Byte().Values(jen.Dict{
						jen.Id("externalStorageRefKey"): jen.Index().Byte().Parens(jen.Id("ref")),
					}),
				})
			})
			g.Return(jen.Id("out"), jen.Nil())
		})

	// Decode
	f.Func().Params(jen.Id("c").Op("*").Id("ExternalStorageCodec")).Id("Decode").
		Params(jen.Id("payloads").Index().Add(payloadPtr)).
		Params(jen.Index().Op("*").Qual(temporalCommon, "Payload"), jen.Error()).
		BlockFunc(func(g *jen.Group) {
			g.Id("out").Op(":=").Make(jen.Index().Op("*").Qual(temporalCommon, "Payload"), jen.Len(jen.Id("payloads")))
			g.For(jen.List(jen.Id("i"), jen.Id("p")).Op(":=").Range().Id("payloads")).BlockFunc(func(b *jen.Group) {
				b.List(jen.Id("ref"), jen.Id("ok")).Op(":=").Id("p").Dot("Metadata").Index(jen.Id("externalStorageRefKey"))
				b.If(jen.Op("!").Id("ok")).Block(
					jen.Id("out").Index(jen.Id("i")).Op("=").Id("p"),
					jen.Continue(),
				)
				b.List(jen.Id("raw"), jen.Err()).Op(":=").Id("c").Dot("Store").Dot("Get").Call(jen.String().Parens(jen.Id("ref")))
				b.If(jen.Err().Op("!=").Nil()).Block(jen.Return(jen.Nil(), jen.Err()))
				b.Id("restored").Op(":=").Op("&").Qual(temporalCommon, "Payload").Values()
				b.If(
					jen.Err().Op(":=").Qual(protoPkg, "Unmarshal").Call(jen.Id("raw"), jen.Id("restored")),
					jen.Err().Op("!=").Nil(),
				).Block(jen.Return(jen.Nil(), jen.Err()))
				b.Id("out").Index(jen.Id("i")).Op("=").Id("restored")
			})
			g.Return(jen.Id("out"), jen.Nil())
		})

	f.Comment("NewDataConverter wires the claim-check codec onto Temporal's default data")
	f.Comment("converter. Pass the result as client.Options{DataConverter: ...}.")
	f.Func().Id("NewDataConverter").Params(jen.Id("store").Id("ExternalStore")).Qual(sdkConverter, "DataConverter").Block(
		jen.Return(jen.Qual(sdkConverter, "NewCodecDataConverter").Call(
			jen.Qual(sdkConverter, "GetDefaultDataConverter").Call(),
			jen.Op("&").Id("ExternalStorageCodec").Values(jen.Dict{
				jen.Id("Threshold"): jen.Lit(threshold),
				jen.Id("Store"):     jen.Id("store"),
			}),
		)),
	)

	var buf bytes.Buffer
	if err := f.Render(&buf); err != nil {
		return "", err
	}
	return buf.String(), nil
}
