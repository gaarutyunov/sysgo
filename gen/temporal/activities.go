package temporal

import (
	"bytes"
	"sort"

	"github.com/dave/jennifer/jen"

	"github.com/gaarutyunov/sysgo/engine"
)

// GenerateActivities emits Go source for the activity layer of the model:
// the model structs referenced by activity parameters, an Activities interface
// (the hexagonal outbound port) with a method per @Activity action, and an
// Options map carrying each activity's Temporal configuration.
//
// Activity bodies are not generated here — the interface is the port; a later
// slice emits hand-written stubs and wires the Temporal SDK options.
func GenerateActivities(m *engine.Model, packageName string) (string, error) {
	acts := Activities(m)
	sort.Slice(acts, func(i, j int) bool { return acts[i].Name() < acts[j].Name() })

	f := jen.NewFile(packageName)
	f.HeaderComment("Code generated by sysgo (gen/temporal). DO NOT EDIT.")

	// Model structs referenced by activity parameters (deduplicated, sorted).
	emitStructs(f, collectParamStructs(acts))

	// The activities outbound-port interface.
	f.Type().Id("Activities").InterfaceFunc(func(g *jen.Group) {
		for _, a := range acts {
			g.Id(exported(a.Name())).ParamsFunc(func(p *jen.Group) {
				p.Id("ctx").Qual("context", "Context")
				for _, param := range attributes(a.Element) {
					typeName, _, _ := goType(param)
					p.Id(param.Name()).Id(typeName)
				}
			}).Error()
		}
	})

	// Per-activity Temporal options.
	emitOptions(f, acts)

	var buf bytes.Buffer
	if err := f.Render(&buf); err != nil {
		return "", err
	}
	return buf.String(), nil
}

// collectParamStructs returns the item/attribute definitions used as activity
// parameter types, plus their nested definition types, deduplicated by
// qualified name.
func collectParamStructs(acts []Activity) []engine.Element {
	seen := map[string]bool{}
	var order []engine.Element
	var add func(def engine.Element)
	add = func(def engine.Element) {
		qn := def.QualifiedName()
		if seen[qn] {
			return
		}
		seen[qn] = true
		order = append(order, def)
		for _, attr := range attributes(def) {
			if _, sd, isStruct := goType(attr); isStruct {
				add(sd)
			}
		}
	}
	for _, a := range acts {
		for _, param := range attributes(a.Element) {
			if _, sd, isStruct := goType(param); isStruct {
				add(sd)
			}
		}
	}
	sort.Slice(order, func(i, j int) bool { return order[i].Name() < order[j].Name() })
	return order
}

func emitStructs(f *jen.File, defs []engine.Element) {
	for _, def := range defs {
		f.Type().Id(exported(def.Name())).StructFunc(func(g *jen.Group) {
			for _, attr := range attributes(def) {
				typeName, _, _ := goType(attr)
				g.Id(exported(attr.Name())).Id(typeName)
			}
		})
	}
}

func emitOptions(f *jen.File, acts []Activity) {
	f.Type().Id("ActivityOptions").Struct(
		jen.Id("TaskQueue").String(),
		jen.Id("MaxAttempts").String(),
		jen.Id("InitialInterval").String(),
		jen.Id("StartToClose").String(),
		jen.Id("ScheduleToClose").String(),
		jen.Id("Heartbeat").String(),
		jen.Id("Idempotent").Bool(),
	)

	f.Var().Id("Options").Op("=").Map(jen.String()).Id("ActivityOptions").Values(jen.DictFunc(func(d jen.Dict) {
		for _, a := range acts {
			d[jen.Lit(exported(a.Name()))] = jen.Values(jen.DictFunc(func(o jen.Dict) {
				if a.TaskQueue != "" {
					o[jen.Id("TaskQueue")] = jen.Lit(a.TaskQueue)
				}
				if a.Idempotent {
					o[jen.Id("Idempotent")] = jen.True()
				}
				if a.Retry != nil {
					putIf(o, "MaxAttempts", a.Retry.MaxAttempts)
					putIf(o, "InitialInterval", a.Retry.InitialInterval)
				}
				if a.Timeout != nil {
					putIf(o, "StartToClose", a.Timeout.StartToClose)
					putIf(o, "ScheduleToClose", a.Timeout.ScheduleToClose)
					putIf(o, "Heartbeat", a.Timeout.Heartbeat)
				}
			}))
		}
	}))
}

func putIf(o jen.Dict, field, value string) {
	if value != "" {
		o[jen.Id(field)] = jen.Lit(value)
	}
}
