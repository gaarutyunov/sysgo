package temporal

import (
	"bytes"
	"sort"

	"github.com/dave/jennifer/jen"

	"github.com/gaarutyunov/sysgo/engine"
)

// Handler is a @Signal- or @Query-annotated action and its channel/query name.
type Handler struct {
	Element engine.Element
	Name    string // the signal/query name from the annotation, or the element name
}

// Signals returns the @Signal-annotated actions in the model's user packages.
func Signals(m *engine.Model) []Handler { return handlers(m, "Signal") }

// Queries returns the @Query-annotated actions in the model's user packages.
func Queries(m *engine.Model) []Handler { return handlers(m, "Query") }

func handlers(m *engine.Model, kind string) []Handler {
	var out []Handler
	walkUser(m, func(e engine.Element) {
		meta, ok := e.Metadata(kind)
		if !ok {
			return
		}
		name := val(meta, "name")
		if name == "" {
			name = e.Name()
		}
		out = append(out, Handler{Element: e, Name: name})
	})
	sort.Slice(out, func(i, j int) bool { return out[i].Element.Name() < out[j].Element.Name() })
	return out
}

// GenerateHandlers emits Go source for the signal and query handler ports of the
// model (specs/TEMPORAL.md §3): a Signals interface (method per @Signal action)
// and a Queries interface (method per @Query action), plus the model structs
// their parameters reference, and Names maps from method name to the declared
// signal/query name.
//
// This is the SDK-free handler surface. Wiring these ports to
// workflow.GetSignalChannel / workflow.SetQueryHandler, and mapping state defs +
// transitions to selector-loop state machines, are deferred — the latter needs
// engine modeling of accept-signal / transition behavior (a prerequisite).
func GenerateHandlers(m *engine.Model, packageName string) (string, error) {
	sigs := Signals(m)
	queries := Queries(m)

	f := jen.NewFile(packageName)
	f.HeaderComment("Code generated by sysgo (gen/temporal). DO NOT EDIT.")

	emitStructs(f, handlerParamStructs(append(append([]Handler{}, sigs...), queries...)))
	emitHandlerInterface(f, "Signals", sigs)
	emitHandlerInterface(f, "Queries", queries)
	emitNames(f, "SignalNames", sigs)
	emitNames(f, "QueryNames", queries)

	var buf bytes.Buffer
	if err := f.Render(&buf); err != nil {
		return "", err
	}
	return buf.String(), nil
}

func emitHandlerInterface(f *jen.File, name string, hs []Handler) {
	f.Type().Id(name).InterfaceFunc(func(g *jen.Group) {
		for _, h := range hs {
			g.Id(exported(h.Element.Name())).ParamsFunc(func(p *jen.Group) {
				p.Id("ctx").Qual("context", "Context")
				for _, param := range attributes(h.Element) {
					typeName, _, _ := goType(param)
					p.Id(param.Name()).Id(typeName)
				}
			}).Error()
		}
	})
}

func emitNames(f *jen.File, varName string, hs []Handler) {
	f.Var().Id(varName).Op("=").Map(jen.String()).String().Values(jen.DictFunc(func(d jen.Dict) {
		for _, h := range hs {
			d[jen.Lit(exported(h.Element.Name()))] = jen.Lit(h.Name)
		}
	}))
}

// handlerParamStructs collects the item/attribute definitions used as handler
// parameter types (and their nested types), deduplicated and sorted.
func handlerParamStructs(hs []Handler) []engine.Element {
	seen := map[string]bool{}
	var order []engine.Element
	var add func(def engine.Element)
	add = func(def engine.Element) {
		qn := def.QualifiedName()
		if seen[qn] {
			return
		}
		seen[qn] = true
		order = append(order, def)
		for _, attr := range attributes(def) {
			if _, sd, isStruct := goType(attr); isStruct {
				add(sd)
			}
		}
	}
	for _, h := range hs {
		for _, param := range attributes(h.Element) {
			if _, sd, isStruct := goType(param); isStruct {
				add(sd)
			}
		}
	}
	sort.Slice(order, func(i, j int) bool { return order[i].Name() < order[j].Name() })
	return order
}
